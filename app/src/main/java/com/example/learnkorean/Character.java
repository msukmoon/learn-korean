package com.example.learnkorean;

public class Character {

    private String korean;
    private String english;

    public Character(String iKorean, String iEnglish) {
        korean = iKorean;
        english = iEnglish;
    }

    public String getKorean() {
        return korean;
    }

    public String getEnglish() {
        return english;
    }

}
